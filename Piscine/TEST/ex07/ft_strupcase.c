#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c >= 'a' && c <= 'z')
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
