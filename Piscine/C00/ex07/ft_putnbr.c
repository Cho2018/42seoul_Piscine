#include <unistd.h>

void	cal(int nb)
{
	char c;

	if (nb == 0)
		return ;
	cal(nb / 10);
	c = '0' + nb % 10;
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	char c;

	if (nb >= 0)
	{
		cal(nb / 10);
		c = '0' + nb % 10;
	}
	else
	{
		write(1, "-", 1);
		cal(-(nb / 10));
		c = '0' - nb % 10;
	}
	write(1, &c, 1);
}
