#include <unistd.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, char *src);

int main()
{
	char dest[100];
	char *src;

	src = "Friday Saturday";
	ft_strcpy(dest, src);
	printf("%s\n", dest);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
